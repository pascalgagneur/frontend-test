;(function ($) {
    "use strict";
    var quickContactForm = $('#quickcontactform');
    //Enable placeholders
    $('input, textarea').placeholder();

    //Remove native validation
    quickContactForm.attr('novalidate', 'novalidate');

    quickContactForm.submit(function(event) {
        var anyError = false,
            anyCharPatt = /\w+/gi,
            emailPatt = /^(([^<>()[\]\\.,;:\s@\"]+(\.[^<>()[\]\\.,;:\s@\"]+)*)|(\".+\"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
            urlPatt = /^(http|https):\/\/[-a-zA-Z0-9@:%_\+.~#?&//=]{2,256}\.[a-z]{2,4}\b(\/[-a-zA-Z0-9@:%_\+.~#?&//=]*)?/gi,
            nameInput = $("#quick-contact-fullname"),
            emailInput = $("#quick-contact-email"),
            urlInput = $("#quick-contact-website"),
            msgInput = $("#quick-contact-msg");

        function correctValue(elm, patt) {
            //console.log(elm.val().match(patt));
            //console.log(patt.test(elm.val()));
            if (!elm.val().match(patt)){
                elm.addClass('error');
                elm.keyup(function () {
                    console.log(patt);
                    correctValue(elm, patt);
                });
                anyError = true;
            } else {
                elm.unbind('keyup');
                elm.removeClass('error');
            }
        }

        correctValue(nameInput, anyCharPatt);
        correctValue(msgInput, anyCharPatt);
        correctValue(emailInput, emailPatt);
        correctValue(urlInput, urlPatt);

        if (anyError) {
            alert('Please check your values');
        }
        return !anyError;
    });

}(jQuery))

